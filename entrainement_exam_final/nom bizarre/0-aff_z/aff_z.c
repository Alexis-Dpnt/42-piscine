int main(int argc, char **argv)
{
	if (argc != 2)
	{
		write(1, "z", 1);
		return (1);
	}

	if (argv[1][0] == '\0')
	{
		write(1, "z", 1);
		return (1);
	}

	write(1, "z", 1);
	write(1, "\n", 1);
}